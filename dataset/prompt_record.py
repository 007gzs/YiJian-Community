from typing import Dict


class Txt2TxtRecord:
    """
    used for tasks where the input and the output are both text.
    """

    def __init__(
        self,
        prompt_text: str,
        response_text: str = None,
        risk_type: Dict = None,
        lang: str = "zh",
    ) -> None:
        """

        Args:
            prompt_text (str): text prompt for large models.
            response_text (str, optional): text response generated by large models. Defaults to None.
            risk_type (Dict, optional): details are in risk_category_zh.json and risk_category_en.json. Defaults to None.
            lang (str, optional): the language of the text, can be "zh" (chinese) or "en" (English). Defaults to "zh".
        """
        self.prompt_text = prompt_text
        self.response_text = response_text
        self.risk_type = risk_type
        self.lang = lang

    def __str__(self) -> str:
        return f"Txt2TxtRecord(prompt_text={self.prompt_text}, response_text={self.response_text})"


